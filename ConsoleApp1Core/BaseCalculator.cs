namespace ConsoleApp1Core
{
	public class BaseCalculator
	{
		public int Add(int value1, int value2) => value1 + value2;
		public int Substruct(int value1, int value2) => value1 - value2;
		public int Multiply(int value1, int value2) => value1 * value2;
		public int Devide(int value1, int value2) => value1 / value2;
	}
}
